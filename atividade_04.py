# Crie um programa que receba três notas de um aluno e calcule a média aritmética.
nota1 = float(input("digete a primeira nota: "))
nota2 = float(input("digete a segunda nota: "))
nota3 = float(input("digete a terceira nota: "))

media = (nota1 + nota2 +nota3)/3

print(f" a média arimética das notas é: {media}")

